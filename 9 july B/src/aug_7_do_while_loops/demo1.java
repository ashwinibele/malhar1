package aug_7_do_while_loops;     

public class demo1     //do while loop print num from 1to5
{
  
	public static void main (String[]args)
	{
		  int i=1;         //  start num
	
	do
	{
	    System.out.println(i);         //1 2 3 4 5
	
	        i++;                      //6     //incr or decr
	}
	 while (i<=5);              //6<=5
	
	
	}	
	
}
