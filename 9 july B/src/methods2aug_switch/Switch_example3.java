package methods2aug_switch;

public class Switch_example3 {
public static void main(String[] args) 
{
	switch ("cw")
	{
	case "cd":
	System.out.println("running output for cd");   
	break;
	
	case "cw":
	System.out.println("running output for cw");  
	break;
	
	case "mt":
	System.out.println("running output for mt");
	break;
	
	case "be":
	System.out.println( "running output for be");	
	break;
	
	case "pc":	
	System.out.println("running output for pc");	
	break;
	
	default : System.out.println("invalid input");
	}
	
	}
}
