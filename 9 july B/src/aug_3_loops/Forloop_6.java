package aug_3_loops;

public class Forloop_6
{
 public static void main(String[]args)
 { 
	 int b= 4;    //user input
	 for (int i =1; i<=10; i++ )       //for (initialisation; test expression;increament)
	 {
		 System.out.println(b*i);    //output statement
	 }
                                    //logic for printing table (b*i)
		 
 
 }

 
 
 
 
 }
